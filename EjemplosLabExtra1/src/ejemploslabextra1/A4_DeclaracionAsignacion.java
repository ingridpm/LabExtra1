package ejemploslabextra1;

public class A4_DeclaracionAsignacion {
    public static void main(String[] args) {
        //Declaración de una variable "int" (entero) que guarda mi edad
        int edad;
        
        //Asignación de un valor a la variable edad
        edad = 22;
        
        //Acceder al valor de la variable edad
        System.out.println(edad);
        
        ///------------------------------------------------\\\
        //Declaración y asignación en la misma línea
        int altura = 160;
        //Acceder al valor de la variable altura
        System.out.println(altura);
    }
}
